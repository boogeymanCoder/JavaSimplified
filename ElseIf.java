public class ElseIf {
    public static void main(String[] args) {
        if (1 == 2) {
            System.out.println("1 == 2 is true!");
        } else if (1 == 1) {
            System.out.println("1 == 1 is true!");
        } else {
            System.out.println("It is false!");
        }
    }
}
